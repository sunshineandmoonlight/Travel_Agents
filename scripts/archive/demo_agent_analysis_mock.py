def print_header(title):
    """打印标题"""
    print("\n" + "=" * 80)
    print(f"  {title}")
    print("=" * 80)


def print_agent_header(name, status, duration):
    """打印Agent头部信息"""
    status_icon = {
        "completed": "[OK]",
        "running": "[...]",
        "error": "[ERR]",
        "skipped": "[SKIP]"
    }
    print(f"\n{status_icon.get(status, '[?]')} {name}")
    print(f"   状态: {status.upper()} | 耗时: {duration}ms")


def print_agent_report(report):
    """打印Agent分析报告"""
    print("\n" + "-" * 80)
    print(report)


